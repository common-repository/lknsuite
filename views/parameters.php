<h1>Parameters</h1>
<p>
    <ul>
        <li>{POST_TITLE} : Wordpress post title</li>
        <li>{SITE_NAME} : Wordpress sitename</li>
        <li>{POST_CONTENT} : Post content</li>
        <li>{POST_CONTENT_100} : The first 100 letters of the post content (you can change 100 to whatever you want)</li>
        <li>{WORDS_5} : The first 5 words of the post content (you can change 5 to
            whatever you want)
        </li>
        <li>{POST_URL} : Post url (Please note that social media site do not accepts localhost urls. That means if you are trying to post something like "http://localhost/wp/2017/05/18/your-slug/" , Social media site will return error.  )</li>
        <li>{TAGS} : Post tags</li>
        <li>{CATS} : Post category names</li>
    </ul>
</p>

<p class="description"><strong>Instagram Note: You will need to add featured image for "Wordpress to
        Instagram".</strong></p>


